// Add this helper function
const boldMyName = (authors: string) => {
  // Using a special delimiter that we can split on later
  const namesToBold = ['Zan Gojcic'];
  
  let result = authors;
  namesToBold.forEach(name => {
    result = result.replace(name, `|||${name}|||`);
  });
  return result;
};

